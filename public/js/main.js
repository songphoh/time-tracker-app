function showTime() {
  // ‡πÉ‡∏ä‡πâ Date ‡πÉ‡∏´‡∏°‡πà
  var date = new Date();
  
  // ‡∏î‡∏∂‡∏á‡∏Ñ‡πà‡∏≤‡∏ä‡∏î‡πÄ‡∏ä‡∏¢‡πÄ‡∏ß‡∏•‡∏≤‡∏à‡∏≤‡∏Å localStorage (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ)
  var timeOffset = localStorage.getItem('time_offset') || 0;
  timeOffset = parseInt(timeOffset);
  
  // ‡∏õ‡∏£‡∏±‡∏ö‡πÄ‡∏ß‡∏•‡∏≤‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏ß‡∏•‡∏≤‡∏ó‡πâ‡∏≠‡∏á‡∏ñ‡∏¥‡πà‡∏ô‡∏Å‡πà‡∏≠‡∏ô (UTC+0)
  var utcHours = date.getUTCHours();
  var utcMinutes = date.getUTCMinutes();
  var utcSeconds = date.getUTCSeconds();
  
  // ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÄ‡∏ß‡∏•‡∏≤‡πÑ‡∏ó‡∏¢ (UTC+7) ‡πÅ‡∏•‡∏∞‡∏õ‡∏£‡∏±‡∏ö‡∏ï‡∏≤‡∏°‡∏Ñ‡πà‡∏≤‡∏ä‡∏î‡πÄ‡∏ä‡∏¢‡πÄ‡∏ß‡∏•‡∏≤
  var totalMinutes = (utcHours * 60 + utcMinutes) + 420 + timeOffset; // 420 ‡∏Ñ‡∏∑‡∏≠ 7 ‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á‡πÄ‡∏õ‡πá‡∏ô‡∏ô‡∏≤‡∏ó‡∏µ
  var h = Math.floor(totalMinutes / 60) % 24; // ‡∏ó‡∏≥‡πÉ‡∏´‡πâ‡∏ä‡∏±‡πà‡∏ß‡πÇ‡∏°‡∏á‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏ä‡πà‡∏ß‡∏á 0-23
  var m = totalMinutes % 60;
  var s = utcSeconds;
  
  var dot = (s % 2 === 0) ? '\xa0' : '.'; // ‡∏Å‡∏£‡∏∞‡∏û‡∏£‡∏¥‡∏ö‡∏à‡∏∏‡∏î‡∏ó‡∏∏‡∏Å‡∏ß‡∏¥‡∏ô‡∏≤‡∏ó‡∏µ

  // ‡∏à‡∏±‡∏î‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡πÄ‡∏ß‡∏•‡∏≤‡πÉ‡∏´‡πâ‡∏°‡∏µ 2 ‡∏´‡∏•‡∏±‡∏Å‡πÄ‡∏™‡∏°‡∏≠
  h = h < 10 ? "0" + h : h;
  m = m < 10 ? "0" + m : m;
  s = s < 10 ? "0" + s : s;

  var time = h + ":" + m + ":" + s + dot;
  
  // ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡πÉ‡∏´‡πâ element ‡∏ó‡∏µ‡πà‡∏°‡∏µ id ‡πÄ‡∏õ‡πá‡∏ô MyClockDisplay ‡πÅ‡∏™‡∏î‡∏á‡πÄ‡∏ß‡∏•‡∏≤
  var clockDisplay = document.getElementById("MyClockDisplay");
  if (clockDisplay) {
    clockDisplay.innerText = time;
    clockDisplay.textContent = time;
  }

  setTimeout(showTime, 1000);
}

